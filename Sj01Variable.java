public class Sj01Variable
{
  public static void main(String[] args)
  {
    boolean bool1 = true, bool2 = false;
    System.out.println("bool1 = " + bool1 + " \tboo2 = " + bool2);
    
    char ch = 'A';
    System.out.println("ch = \t" + ch);
    
    byte byte1 = 127;
    System.out.println("byte1 = \t" + byte1);
    
    short short1 = 32767;
    System.out.println("short1 = \t" + short1);
    
    int int1 = 2147483647;
    System.out.println("int1 = \t" + int1);
    
    long long1 = 9223372036854775807L;
    System.out.println("long1 \t" + long1);
    
    float float1 = +3.4E+38f;
    System.out.println("float1 = \t" + float1);
    
    double double1 = 1.79E+308;
    System.out.println("double1 = \t" + double1);
    
    double 변수 = 123.45E+02;
    System.out.println(" 변수의 값은 = " + 변수);
  }
}
